function initializeSlider1(){
    const slider = document.getElementById('slider1');
    const prevButton = document.getElementById('prev1');
    const nextButton = document.getElementById('next1');

    let slideIndex = 0;

    prevButton.addEventListener('click', () => {
        slideIndex = (slideIndex - 1 + slider.childElementCount) % slider.childElementCount;
        updateSlider();
    });

    nextButton.addEventListener('click', () => {
        slideIndex = (slideIndex + 1) % slider.childElementCount;
        updateSlider();
    });

    function updateSlider() {
        slider.style.transform = `translateX(-${slideIndex * 100}%)`;
    }

    setInterval(() => {
        slideIndex = (slideIndex + 1) % slider.childElementCount;
        updateSlider();
    }, 3000);
}

